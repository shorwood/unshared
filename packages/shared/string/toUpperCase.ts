/**
 * Converts a string to lower case.
 * @param value The string to convert
 * @return The converted string
 * @example
 * ```
 * toCamelCase('foo_bar') // returns 'FOO_BAR'
 * toCamelCase('FOO_BAR') // returns 'FOO_BAR'
 * ```
 */
export const toUpperCase = (value: string): string => value.toUpperCase()
