import { expect, it } from 'vitest'
import { isStringTimestamp } from './isStringTimestamp'

it.each([

  // --- Returns true
  [true, '1994-11-05T13:15:30Z'],
  [true, '1994-11-05T08:15:30-05:00'],
  [true, '1994-11-05T08:15:30+05:00'],
  [true, '1994-11-05'],
  [true, '19941105T131530Z'],
  [true, '1994-W48-5'],
  [true, '1994W485'],
  [true, '1994-135'],
  [true, '1994-W15-5T13:15:30Z'],
  [true, '1994W155T131530Z'],
  [true, '1994-W15-5'],
  [true, '1994W155'],
  [true, '1994-05'],
  [true, '199405'],
  [true, '1994-05-05T08:15:30-05:00'],
  [true, '19940505T131530Z'],
  [true, '1994-05-05'],
  [true, '19940505'],
  [true, '1994137'],
  [true, '1994-11-05T13:15:30'],
  [true, '1994-11-05T081530+05:00'],
  [true, '1994-11-05T08:15:30+05'],
  [true, '1994-11-05T08:15:30.045+05:00'],
  [true, '19941105T131530+05:00'],
  [true, '19941105T131530.045+0500'],
  [true, '1994-W48'],
  [true, '1994W48'],
  [true, '1994W48-5'],
  [true, '1994-W15'],
  [true, '1994W15'],
  [true, '1994W15-5T13:15:30Z'],
  [true, '1994W15-5T131530Z'],
  [true, '19940505T081530-05:00'],
  [true, '19940505T08:15:30-05:00'],

  // --- Returns false
  [false, '1994-11-05-'],
  [false, '19941105T131530.45+0500'],
  [false, '19941105T131530.45'],
  [false, '19941350'],
  [false, '1994-11-05T08:15:30.45'],
  [false, '1994-11-05T08:15:30.45+05:00'],
  [false, '1994W485-'],
  [false, '1994-135T'],
  [false, '1994135T'],
  [false, '19941350T'],
  [false, '1994137T131530Z'],
  [false, '1994-11-05 08:15:30-05:00'],
  [false, '19941105T131530.45+05'],
  [false, '1994W155T131530Z-'],
  [false, '1994W155-'],
  [false, '1994-05-'],
  [false, '199405-'],
  [false, '19940505081530-05:00'],
  [false, '19940505T081530Z-05:00'],
  [false, '19940505-'],
  [false, '1994137T131530+05:00'],
  [false, '19941370T131530Z'],
  [false, '19941370T131530Z-'],
  [false, '19941370-'],

])('should return %s when checking if %s is a string representing a timestamp', (expected, value: any) => {
  const result = isStringTimestamp(value)
  expect(result).toEqual(expected)
})
