/** Matches integer */
export const integer = /^-?\d+$/

/** Matches positive integer */
export const integerPositive = /^\d+$/

/** Matches negative integer */
export const integerNegative = /^-\d+$/

/** Matches negative float */
export const float = /^-\d*\.?\d+$/

/** Matches positive float */
export const floatPositive = /^\d*\.?\d+$/

/** Matches negative float */
export const floatNegative = /^-\d*\.?\d+$/
